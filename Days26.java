package day26coding;
public class Day26Coding {
    public static void main(String[] args) {
        int x = 10;
        System.out.println("Nilai x = "+x);
        x += 5;
        System.out.println("Nilai x += "+x);
        x -= 5;
        System.out.println("Nilai x -= "+x);
        x *= 5;
        System.out.println("Nilai x *= "+x);
        x /= 5;
        System.out.println("Nilai x /= "+x);
        x %= 5;
        System.out.println("Nilai x %= "+x);
    }
}
