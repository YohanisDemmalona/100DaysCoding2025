package day70coding;
public class day77Coding {
    public static void main(String[] args) {
        String nama = " Yohanis ";

        // substring() - mengambil substring dari string
        System.out.println(nama.substring(1, 5));

        // replace() - mengganti karakter atau substring dalam string
        System.out.println(nama.replace(" ", "_")); 

        // trim() - menghapus whitespace di awal dan akhir string (spasi)
        System.out.println(nama.trim()); 
 
    }
}
