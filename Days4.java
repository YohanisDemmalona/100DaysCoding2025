public class Day4Coding {
    public static void main(String[] args) {
        /*
        contoh Escape Sequence
        New line (\n) Baris Baru
        */        
        System.out.println("Hello\nWorld!");
        // Tab (\t) Tab
        System.out.println("Nama\tYohanis Demmalona");
        System.out.println("Umur\t19");
        // Backslash (\\) 
        System.out.println("C:\\Users\\ADVAN\\Documents\\NetBeansProjects");
         // Double quote (\") Kutip ganda
        System.out.println("Dia berkata, \"Hello World!\"");
        // Single quote (\') Kutip tunggal
        System.out.println("Saya kuliah di hari Jum'at");
    }   
}
