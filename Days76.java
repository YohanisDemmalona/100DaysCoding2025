package day70coding;
public class day76Coding {
    public static void main(String[] args) {    
        String nama = "Anis";
        System.out.println(nama.equals("Anis")); // true
        System.out.println(nama.equalsIgnoreCase("ANIS")); // true
        System.out.println(nama.contains("ANis")); // true
        System.out.println(nama.isEmpty()); // false
        System.out.println("".isEmpty()); // true
    }
}
