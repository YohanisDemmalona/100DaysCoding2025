package day5coding;
public class Day5Coding {
    public static void main(String[] args) {
       byte nilaiSatu = 100;
        short nilaiDua = 20000;
        int nilaiTiga = 1000000;
        long nilaiEmpat = 1000000000L;
        System.out.println("Nilai byte: " + nilaiSatu);
        System.out.println("Nilai short: " + nilaiDua);
        System.out.println("Nilai int: " + nilaiTiga);
        System.out.println("Nilai long: " + nilaiEmpat);
    } 
}
